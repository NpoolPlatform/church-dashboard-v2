<template>
  <q-table
    dense
    flat
    :title='$t("MSG_REVIEW_TIMEOUT_PAYMENTS")'
    :rows='payments'
    row-key='ID'
    :loading='paymentLoading'
    :rows-per-page-options='[10]'
  />
  <q-table
    dense
    flat
    :title='$t("MSG_REVIEW_USER_PAYMENT_BALANCES")'
    :rows='myBalances'
    row-key='ID'
    :loading='balanceLoading'
    :rows-per-page-options='[10]'
  />
</template>

<script setup lang='ts'>
import { NotificationType, Payment, useChurchBillingStore, UserPaymentBalance } from 'npool-cli-v2'
import { useChurchAppCoinStore } from 'npool-cli-v4'
import { getAppCoins } from 'src/api/coin'
import { useLocalApplicationStore } from 'src/localstore'
import { computed, onMounted, ref, watch } from 'vue'
import { useI18n } from 'vue-i18n'

// eslint-disable-next-line @typescript-eslint/unbound-method
const { t } = useI18n({ useScope: 'global' })

const app = useLocalApplicationStore()
const appID = computed(() => app.AppID)

const billing = useChurchBillingStore()
const payments = computed(() => billing.Payments.get(appID.value) as Array<Payment>)
const balances = computed(() => billing.PaymentBalances.get(appID.value))

interface MyBalance extends UserPaymentBalance {
  CoinName: string
  CoinUSDCurrency: number
  USDAmount: number
}
const myBalances = computed(() => {
  const bls = [] as Array<MyBalance>
  balances.value?.forEach((balance) => {
    const el = balance as MyBalance
    const index = payments.value?.findIndex((pay) => pay.ID === el.PaymentID)
    if (index === undefined) {
      return
    }
    if (index < 0) {
      return
    }
    const payment = payments.value[index]
    el.CoinName = coin.getCoinByID(appID.value, payment.CoinInfoID)?.Name as string
    el.CoinUSDCurrency = payment.CoinUSDCurrency
    el.USDAmount = el.Amount * payment.CoinUSDCurrency
    bls.push(el)
  })
  return bls
})

const coin = useChurchAppCoinStore()
const coins = computed(() => coin.getCoinsByAppID(appID.value))

const paymentLoading = ref(false)
const balanceLoading = ref(false)

watch(appID, () => {
  prepare()
})

onMounted(() => {
  prepare()
})

const prepare = () => {
  paymentLoading.value = true
  balanceLoading.value = true

  billing.getPayments({
    TargetAppID: appID.value,
    Message: {
      Error: {
        Title: t('MSG_GET_PAYMENTS'),
        Message: t('MSG_GET_PAYMENTS_FAIL'),
        Popup: true,
        Type: NotificationType.Error
      }
    }
  }, () => {
    paymentLoading.value = false
  })

  billing.getPaymentBalances({
    TargetAppID: appID.value,
    Message: {
      Error: {
        Title: t('MSG_GET_PAYMENT_BALANCES'),
        Message: t('MSG_GET_PAYMENT_BALANCES_FAIL'),
        Popup: true,
        Type: NotificationType.Error
      }
    }
  }, () => {
    balanceLoading.value = false
  })

  if (coins.value.length === 0) {
    getAppCoins(0, 500)
  }
}
</script>
