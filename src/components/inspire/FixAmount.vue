<template>
  <q-table
    dense
    flat
    :title='$t("MSG_FIX_AMOUNTS")'
    :rows='coupons'
    row-key='ID'
    :loading='loading'
    :rows-per-page-options='[10]'
    @row-click='(evt, row, index) => onRowClick(row as FixAmountCoupon)'
  >
    <template #top-right>
      <div class='row indent flat'>
        <q-btn
          dense
          flat
          class='btn flat'
          :label='$t("MSG_CREATE")'
          @click='onCreate'
        />
      </div>
    </template>
  </q-table>
  <q-dialog
    v-model='showing'
    @hide='onMenuHide'
    position='right'
  >
    <q-card class='popup-menu'>
      <q-card-section>
        <span>{{ $t('MSG_CREATE_FIX_AMOUNT') }}</span>
      </q-card-section>
      <q-card-section>
        <q-input v-model='target.Name' :label='$t("MSG_COUPON_NAME")' />
        <q-input v-model='target.Message' :label='$t("MSG_COUPON_DESCRIPTION")' />
        <q-input type='date' v-model='start' :label='$t("MSG_START")' />
        <q-input type='number' v-model='target.DurationDays' :label='$t("MSG_DURATION_DAYS")' :suffix='$t("MSG_DAYS")' />
        <q-input type='number' v-model='target.Circulation' :label='$t("MSG_COUPON_CIRCULATION")' :suffix='PriceCoinName' />
        <q-input type='number' v-model='target.Denomination' :label='$t("MSG_COUPON_DENOMINATION")' :suffix='PriceCoinName' />
      </q-card-section>
      <q-item class='row'>
        <q-btn class='btn round alt' :label='$t("MSG_SUBMIT")' @click='onSubmit' />
        <q-btn class='btn round' :label='$t("MSG_CANCEL")' @click='onCancel' />
      </q-item>
    </q-card>
  </q-dialog>
</template>

<script setup lang='ts'>
import {
  NotificationType,
  formatTime,
  useChurchFixAmountStore,
  useFixAmountStore,
  FixAmountCoupon,
  PriceCoinName
} from 'npool-cli-v2'
import { computed, onMounted, watch, ref } from 'vue'
import { useLocalApplicationStore } from '../../localstore'
import { useI18n } from 'vue-i18n'
import { useLocalUserStore } from 'npool-cli-v4'

// eslint-disable-next-line @typescript-eslint/unbound-method
const { t } = useI18n({ useScope: 'global' })

const app = useLocalApplicationStore()
const appID = computed(() => app.AppID)

const coupon = useChurchFixAmountStore()
const acoupon = useFixAmountStore()
const coupons = computed(() => coupon.FixAmounts.get(appID.value) ? coupon.FixAmounts.get(appID.value) : [])
const loading = ref(true)

const logined = useLocalUserStore()

const prepare = () => {
  loading.value = true
  coupon.getFixAmounts({
    TargetAppID: appID.value,
    Message: {
      Error: {
        Title: t('MSG_GET_FIX_AMOUNTS'),
        Message: t('MSG_GET_FIX_AMOUNTS_FAIL'),
        Popup: true,
        Type: NotificationType.Error
      }
    }
  }, () => {
    loading.value = false
  })
}

watch(appID, () => {
  prepare()
})

onMounted(() => {
  prepare()
})

const showing = ref(false)
const updating = ref(false)
const target = ref({
  ReleaseByUserID: logined.User?.ID
} as unknown as FixAmountCoupon)
const start = computed({
  get: () => formatTime(target.value.Start, true)?.replace(/\//g, '-'),
  set: (val) => {
    target.value.Start = new Date(val).getTime() / 1000
  }
})

const onCreate = () => {
  showing.value = true
  updating.value = false
}

const onRowClick = (coupon: FixAmountCoupon) => {
  showing.value = true
  updating.value = true
  target.value = coupon
}

const onMenuHide = () => {
  showing.value = false
  target.value = {
    ReleaseByUserID: logined.User?.ID
  } as unknown as FixAmountCoupon
}

const onSubmit = () => {
  showing.value = false

  if (updating.value) {
    acoupon.updateFixAmount({
      Info: target.value,
      Message: {
        Error: {
          Title: 'MSG_UPDATE_FIX_AMOUNT',
          Message: 'MSG_UPDATE_FIX_AMOUNT_FAIL',
          Popup: true,
          Type: NotificationType.Error
        }
      }
    }, () => {
      // TODO
    })
    return
  }

  coupon.createFixAmount({
    TargetAppID: appID.value,
    Info: target.value,
    Message: {
      Error: {
        Title: 'MSG_CREATE_FIX_AMOUNT',
        Message: 'MSG_CREATE_FIX_AMOUNT_FAIL',
        Popup: true,
        Type: NotificationType.Error
      }
    }
  }, () => {
    // TODO
  })
}

const onCancel = () => {
  showing.value = false
}

</script>
